<?php

namespace App\Http\Controllers;

use Illuminate\Support\Facades\Auth;

class ApiController extends Controller
{
    protected function respondWithToken($token)
    {
        return response()->json([
            'token' => $token,
            'token_type' => 'bearer',
            'expires_in' => Auth::factory()->getTTL() * 60
        ], 200);
    }
}
